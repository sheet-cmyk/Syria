// ── Custom Delegate ──
class KurdishLocalizationsDelegate
    extends LocalizationsDelegate<AppLocalizations> {
  final String langCode;
  const KurdishLocalizationsDelegate(this.langCode);

  @override
  bool isSupported(Locale locale) => locale.languageCode == langCode;

  @override
  Future<AppLocalizations> load(Locale locale) async {
    if (langCode == 'ckb') return AppLocalizationsCkb();
    return AppLocalizationsKu();
  }

  @override
  bool shouldReload(KurdishLocalizationsDelegate old) => false;
}
